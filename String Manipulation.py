#Concatenation
first_name = "John"
last_name = "Doe"
full_name = first_name + " " + last_name
print("Full Name:", full_name)  # Output: Full Name: John Doe
#Repetition
greeting = "Hi"
repeated_greeting = greeting * 4
print("Repeated Word:", repeated_greeting)  # Output: Repeated Word: HiHiHiHi
#Slicing
message = "Hello World"
sliced_message = message[0:5]
print("Sliced Text:", sliced_message)  # Output: Sliced Text: Hello
#Upper and Lower Case
phrase = "Machine Learning"
upper_phrase = phrase.upper()
lower_phrase = phrase.lower()
print("Upper Case:", upper_phrase)  # Output: Upper Case: MACHINE LEARNING
print("Lower Case:", lower_phrase)  # Output: Lower Case: machine learning

